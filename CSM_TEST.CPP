// Calculate fine divider for AY PSG from output frequency.
uint8_t getAYFinePeriod(uint16_t set_freq)
{
	uint32_t divider;
	if(set_freq==0)
	{
		set_freq = 1;
	}
	divider = AY_INT_FREQ;
	divider = (divider+(set_freq/2))/set_freq;		// Round up
	if(divider>255)
	{
		divider = 255;
	}
	return (uint8_t)divider;
}

